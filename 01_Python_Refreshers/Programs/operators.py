# Operators example
# Operators help us work with values.

print("Addition:", 10 + 3)
print("Subtraction:", 10 - 3)
print("Multiplication:", 10 * 3)
print("Division:", 10 / 3)
print("Modulus:", 10 % 3)
print("Power:", 2 ** 3)

print("Greater than:", 10 > 3)
print("Equal:", 10 == 3)
print("AND:", True and False)
print("OR:", True or False)
print("NOT:", not False)

# Operator precedence shows which operation runs first
print("Precedence 1:", 10 + 3 * 2)
print("Precedence 2:", (10 + 3) * 2)
