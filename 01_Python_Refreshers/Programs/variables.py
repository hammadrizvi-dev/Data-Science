# Variables example
# Variables store values that can be used later in the program.

name = "Ali"       # string variable
age = 25           # integer variable
is_student = True  # boolean variable

print("Name:", name)
print("Age:", age)
print("Student:", is_student)

# You can assign multiple values at once
x, y, z = 10, 20, 30
print("x:", x, "y:", y, "z:", z)

# Swapping two values
first, second = 5, 8
first, second = second, first
print("After swap:", first, second)
